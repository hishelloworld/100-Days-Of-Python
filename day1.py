# Day-01 Band Name Generator

# asking city name from user using input and assigning it a variable

city = input("Hey there! What's the name of your city?\n")

# asking pet name from user using input and assigning it a variable

pet = input("Awesome! Now, what's your pet's name?\n")

# Concatenate the city and pet names to create the band name

band_name = city + " " + pet

# now print the cooncatenated (band_name)...

print("So, your band name could be:  " + band_name)
